### Problem 1

Let ``Y`` be the number of defectives in a box of 50 articles taken from the output of a machine.
Each article is defective with probability 0.01.
Find the probability that ``Y = 0,1,2,`` or ``3``.

(a) By using the binomial distribution.

(b) By using the Poisson distribution.
"""

# ╔═╡ 1f2a358d-fc82-4c00-956e-77a737c776d7
p1_solution = let
	n = 50
	p = 0.01
	Y1 = Binomial(n,p)
	
	λ = n*p
	Y2 = Poisson(λ)
	
	:a => cdf(Y1, 3), :b => cdf(Y2, 3)
end

