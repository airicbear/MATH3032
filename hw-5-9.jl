md"""
### Problem 3

Let ``S^2`` be the sample variance of a random sample of size ``n`` from ``N(\mu, \sigma^2)``.
Show that the limit, as ``n → ∞``, of the mgf of ``S^2`` is ``e^{\sigma^2 t}, \, -∞ < t < ∞``.
Thus, in the limit, the distribution of ``S^2`` is degenerate with probability ``1`` at ``\sigma^2``.
"""

# ╔═╡ 15840b09-8f83-4357-a650-0287019aba50
md"""
**Solution.**

$(n - 1) \frac{S^2}{\sigma^2} ∼ \chi_{n-1}^2 \implies S^2 ∼ \frac{\sigma^2}{n - 1} \chi_{n-1}^2$

$M(t) = \frac{1}{(1 - 2t)^{r/2}} = (1 - 2t)^{-r/2}$

$M_{S^2}(t) = M\left(\frac{\sigma^2}{n - 1} t\right) = \left(1 - \frac{2\sigma^2t}{n - 1}\right)^{-(n - 1)/2}$

$\lim_{n→∞} M_{S^2}(t) = \lim_{n→∞} \left(1 + \frac{\sigma^2 t}{-(n - 1)/2}\right)^{-(n-1)/2} = e^{\sigma^2 t}, \qquad -∞ < t < ∞$
"""

# ╔═╡ 52c5fa2b-4cf2-4714-a484-0f9aa79596fa
md"""
### Problem 5

Let ``Y`` have a Poisson distribution with mean ``3n``.
Use the central limit theorem to show that ``W = (Y - 3n) / \sqrt{3n}`` has a limiting distribution that is ``N(0,1)``.
"""

# ╔═╡ 70476977-096f-4202-967d-fb8fa2617634
md"""
**Solution.**

$Y ∼ \text{Poisson}(3n)$

$\lambda = \mu = \sigma^2 = 3,\quad \sigma = \sqrt{3}$

A distribution of the form

$\frac{\sum_{i=1}^n X_i - n \mu}{\sqrt{n}}$

is approximated as ``N(0,1)`` as ``n → ∞``.

$W = \frac{Y - 3n}{\sqrt{3n}}$

Indeed, ``W`` matches the form and can be approximated as ``N(0,1)``.
"""

